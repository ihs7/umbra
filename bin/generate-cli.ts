import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { join, dirname } from "node:path";
import { loadSpec, extractRoutes } from "../src/openapi";
import type { RouteEntry } from "../src/openapi";

function toKebab(s: string): string {
  return s.replace(/([a-z])([A-Z])/g, "$1-$2").toLowerCase();
}

interface SdkEntry {
  fnName: string;
  url: string;
  method: string;
  description: string;
}

function parseSdk(sdkPath: string): SdkEntry[] {
  const src = readFileSync(sdkPath, "utf-8");
  const entries: SdkEntry[] = [];
  const blocks = src.split(/(?=\/\*\*)/);

  for (const block of blocks) {
    const descMatch = block.match(/\/\*\*\s*\n\s*\*\s*(.+?)\n/);
    const fnMatch = block.match(/export const (\w+)\s*=/);
    const methodMatch = block.match(/\)\s*\.\s*(get|post|put|delete|patch)\s*</);
    const urlMatch = block.match(/url:\s*'([^']+)'/);

    if (descMatch && fnMatch && methodMatch && urlMatch) {
      entries.push({
        description: descMatch[1]!.trim(),
        fnName: fnMatch[1]!,
        method: methodMatch[1]!,
        url: urlMatch[1]!,
      });
    }
  }

  return entries;
}

function parseSchemas(zodPath: string): Set<string> {
  const src = readFileSync(zodPath, "utf-8");
  const names = new Set<string>();
  const regex = /export const (z\w+Data)\s*=/g;
  let match;
  while ((match = regex.exec(src)) !== null) {
    names.add(match[1]!);
  }
  return names;
}

function resourceFromUrl(url: string): string {
  return toKebab(url.replace(/^\//, "").split("/")[0] ?? url);
}

function actionFromUrl(url: string, method: string, fnName: string): string {
  const segments = url
    .replace(/^\//, "")
    .split("/")
    .slice(1)
    .filter((s) => !s.startsWith("{"));

  if (segments.length > 0) return segments.join("-");
  if (fnName.toLowerCase().includes("search") && method === "post") return "search";

  const map: Record<string, string> = {
    get: "list",
    post: "create",
    put: "update",
    delete: "delete",
    patch: "patch",
  };
  return map[method] ?? method;
}

function schemaNameForFn(fnName: string): string {
  return `z${fnName.charAt(0).toUpperCase()}${fnName.slice(1)}Data`;
}

interface GenerateOptions {
  heyApiDir: string;
  sdkImportPrefix?: string;
  packageName?: string;
}

export function generateCliRegistry(options: GenerateOptions): string {
  const { heyApiDir, sdkImportPrefix = "./", packageName = "@ihs7/umbra" } = options;

  const entries = parseSdk(join(heyApiDir, "sdk.gen.ts"));
  const schemas = parseSchemas(join(heyApiDir, "zod.gen.ts"));

  const resources: Record<
    string,
    { action: string; fnName: string; schemaName: string; description: string }[]
  > = {};

  for (const entry of entries) {
    const schemaName = schemaNameForFn(entry.fnName);
    if (!schemas.has(schemaName)) continue;

    const resource = resourceFromUrl(entry.url);
    const action = actionFromUrl(entry.url, entry.method, entry.fnName);

    if (!resources[resource]) resources[resource] = [];
    if (!resources[resource].some((r) => r.action === action)) {
      resources[resource].push({
        action,
        fnName: entry.fnName,
        schemaName,
        description: entry.description,
      });
    }
  }

  const sdkImports: string[] = [];
  const schemaImports: string[] = [];
  const resourceLines: string[] = [];

  for (const [resource, actions] of Object.entries(resources).sort(([a], [b]) =>
    a.localeCompare(b),
  )) {
    const actionLines: string[] = [];

    for (const { action, fnName, schemaName, description } of actions) {
      sdkImports.push(fnName);
      schemaImports.push(schemaName);
      actionLines.push(
        `    "${action}": fromSdk(${schemaName}, ${fnName}, ${JSON.stringify(description)}),`,
      );
    }

    resourceLines.push(`  "${resource}": {\n${actionLines.join("\n")}\n  },`);
  }

  return [
    "// This file is auto-generated by @ihs7/umbra. Do not edit manually.",
    "",
    `import { ${sdkImports.join(", ")} } from "${sdkImportPrefix}sdk.gen";`,
    `import { ${schemaImports.join(", ")} } from "${sdkImportPrefix}zod.gen";`,
    `import { fromSdk } from "${packageName}";`,
    "",
    "export const resources = {",
    ...resourceLines,
    "};",
    "",
  ].join("\n");
}

function parseArgs(argv: string[]): {
  heyApiDir: string;
  outFile: string;
  sdkImportPrefix: string;
  packageName: string;
} {
  let heyApiDir: string | undefined;
  let outFile: string | undefined;
  let sdkImportPrefix = "./";
  let packageName = "@ihs7/umbra";

  for (let i = 0; i < argv.length; i++) {
    const arg = argv[i]!;
    const next = argv[i + 1];
    if (arg === "--hey-api" && next) {
      heyApiDir = next;
      i++;
    } else if (arg === "--out" && next) {
      outFile = next;
      i++;
    } else if (arg === "--sdk-prefix" && next) {
      sdkImportPrefix = next;
      i++;
    } else if (arg === "--package" && next) {
      packageName = next;
      i++;
    }
  }

  if (!heyApiDir) {
    console.error(
      "Usage: zod-cli --hey-api <dir> [--out <path>] [--sdk-prefix <prefix>] [--package <name>]",
    );
    process.exit(1);
  }

  return {
    heyApiDir,
    outFile: outFile ?? join(heyApiDir, "cli.gen.ts"),
    sdkImportPrefix,
    packageName,
  };
}

function needsQuote(key: string): boolean {
  return !/^[a-zA-Z_$][a-zA-Z0-9_$]*$/.test(key);
}

function key(s: string): string {
  return needsQuote(s) ? JSON.stringify(s) : s;
}

function emitParams(params: RouteEntry["params"], indent: string): string {
  if (params.length === 0) return "[]";
  const inner = indent + "  ";
  const items = params.map((p) => {
    const lines = [
      `${inner}  name: ${JSON.stringify(p.name)},`,
      `${inner}  location: "${p.location}",`,
      `${inner}  kind: "${p.kind}",`,
      `${inner}  required: ${p.required},`,
    ];
    if (p.description) lines.push(`${inner}  description: ${JSON.stringify(p.description)},`);
    return `${inner}{\n${lines.join("\n")}\n${inner}}`;
  });
  return `[\n${items.join(",\n")},\n${indent}]`;
}

async function generateSpecModule(source: string, packageName: string): Promise<string> {
  const spec = await loadSpec(source);
  const routes = extractRoutes(spec);

  const lines: string[] = [
    "// Auto-generated by @ihs7/umbra. Do not edit manually.",
    `import { fromResources } from "${packageName}";`,
    `import type { CliCommand, FromSpecOptions, CliConfig } from "${packageName}";`,
    "",
    "const _options: FromSpecOptions = {};",
    "",
    "export function configure(options: FromSpecOptions): void {",
    "  Object.assign(_options, options);",
    "}",
    "",
    "export function getHeaders(): Record<string, string> {",
    "  return _options.headers ?? {};",
    "}",
    "",
    "type HandlerArgs = { path?: Record<string, unknown>; query?: Record<string, unknown>; body?: unknown };",
    "",
    "function makeHandler(",
    "  baseUrl: string,",
    "  path: string,",
    "  method: string,",
    '): CliCommand["handler"] {',
    "  return async (args: HandlerArgs) => {",
    "    const resolved = path.replace(/\\{(\\w+)\\}/g, (_, k) => String((args.path ?? {})[k] ?? ''));",
    "    const url = new URL((_options.baseUrl ?? baseUrl).replace(/\\/$/, '') + resolved);",
    "    for (const [k, v] of Object.entries(args.query ?? {})) url.searchParams.set(k, String(v));",
    "    const hasBody = method !== 'get' && method !== 'delete' && args.body && Object.keys(args.body as object).length > 0;",
    "    const res = await fetch(url.toString(), {",
    "      method: method.toUpperCase(),",
    "      headers: { ...(hasBody ? { 'Content-Type': 'application/json' } : {}), ...(_options.headers ?? {}) },",
    "      ...(hasBody ? { body: JSON.stringify(args.body) } : {}),",
    "    });",
    "    if (!res.ok) { const t = await res.text(); throw new Error(`${res.status} ${res.statusText}: ${t}`); }",
    "    const ct = res.headers.get('content-type') ?? '';",
    "    if (!ct.includes('json')) return res.text();",
    "    const data = await res.json();",
    "    if (Array.isArray(data)) return data;",
    "    if (data && typeof data === 'object' && 'results' in data) return (data as { results: unknown[] }).results;",
    "    return data;",
    "  };",
    "}",
    "",
  ];

  const opNames: Array<{ opName: string; resource: string; action: string }> = [];

  for (const route of routes) {
    const opName = route.operationId
      ? route.operationId.charAt(0).toLowerCase() + route.operationId.slice(1)
      : `${route.action}${route.resource.replace(/-([a-z])/g, (_, c: string) => c.toUpperCase()).replace(/^./, (c: string) => c.toUpperCase())}`;

    opNames.push({ opName, resource: route.resource, action: route.action });

    lines.push(`/**`);
    lines.push(` * ${route.description}`);
    lines.push(` */`);
    lines.push(`export const ${opName}: CliCommand = {`);
    lines.push(`  description: ${JSON.stringify(route.description)},`);
    lines.push(`  params: ${emitParams(route.params, "  ")},`);
    lines.push(
      `  handler: makeHandler(${JSON.stringify(route.defaultBaseUrl)}, ${JSON.stringify(route.path)}, ${JSON.stringify(route.method)}),`,
    );
    lines.push(`};`, ``);
  }

  const byResource = new Map<string, Array<{ opName: string; action: string }>>();
  for (const { opName, resource, action } of opNames) {
    if (!byResource.has(resource)) byResource.set(resource, []);
    byResource.get(resource)!.push({ opName, action });
  }

  lines.push(`const resources: Record<string, Record<string, CliCommand>> = {`);
  for (const [resource, ops] of [...byResource.entries()].sort(([a], [b]) => a.localeCompare(b))) {
    lines.push(`  ${key(resource)}: {`);
    for (const { opName, action } of ops) {
      lines.push(`    ${key(action)}: ${opName},`);
    }
    lines.push(`  },`);
  }
  lines.push(`};`, ``);
  lines.push(`export function createCli(config: CliConfig) {`);
  lines.push(
    `  return fromResources(resources, config, (headers) => Object.assign(_options, { headers }));`,
  );
  lines.push(`}`, ``);

  return lines.join("\n");
}

interface DocEntry {
  resource: string;
  action: string;
  description: string;
  params: RouteEntry["params"];
}

async function docEntriesFromSdk(dir: string): Promise<DocEntry[]> {
  const entries = parseSdk(join(dir, "sdk.gen.ts"));
  const schemas = parseSchemas(join(dir, "zod.gen.ts"));
  const zodModule = await import(join(dir, "zod.gen.ts"));
  const { zodIntrospector } = await import("../src/introspect-zod");

  const seen = new Set<string>();
  const docs: DocEntry[] = [];

  for (const entry of entries) {
    const resource = resourceFromUrl(entry.url);
    const action = actionFromUrl(entry.url, entry.method, entry.fnName);
    const key = `${resource}:${action}`;
    if (seen.has(key)) continue;
    seen.add(key);

    const schemaName = schemaNameForFn(entry.fnName);
    const params =
      schemas.has(schemaName) && zodModule[schemaName]
        ? zodIntrospector.extractParams(zodModule[schemaName])
        : [];

    docs.push({ resource, action, description: entry.description, params });
  }

  return docs;
}

function generateDocs(entries: DocEntry[], cliName: string): string {
  const byResource = new Map<string, DocEntry[]>();
  for (const entry of entries) {
    if (!byResource.has(entry.resource)) byResource.set(entry.resource, []);
    byResource.get(entry.resource)!.push(entry);
  }

  const lines: string[] = [
    `<!-- Auto-generated by @ihs7/umbra. Do not edit manually. -->`,
    `# ${cliName} commands`,
    "",
  ];

  const sorted = [...byResource.entries()].sort(([a], [b]) => a.localeCompare(b));

  lines.push(`| Resource | Actions |`);
  lines.push(`|----------|---------|`);
  for (const [resource, actions] of sorted) {
    const actionList = actions.map((e) => `\`${e.action}\``).join(" ");
    lines.push(`| [${resource}](#${resource}) | ${actionList} |`);
  }

  lines.push("");
  lines.push("---");
  lines.push("");

  for (const [resource, actions] of sorted) {
    lines.push(`## ${resource}`);
    lines.push("");
    for (const entry of actions) {
      lines.push(`### \`${entry.action}\``);
      lines.push("");
      lines.push(entry.description);
      lines.push("");
      if (entry.params.length > 0) {
        const flags = entry.params.map((p) => {
          const flag = `--${p.name.replace(/([a-z])([A-Z])/g, "$1-$2").toLowerCase()}`;
          const token = `${flag} <${p.kind}>`;
          return p.required ? token : `[${token}]`;
        });
        lines.push(`\`${cliName} ${resource} ${entry.action} ${flags.join(" ")}\``);
        lines.push("");
        lines.push(`| Flag | Type | Required | Location | Description |`);
        lines.push(`|------|------|----------|----------|-------------|`);
        for (const p of entry.params) {
          const flag = `--${p.name.replace(/([a-z])([A-Z])/g, "$1-$2").toLowerCase()}`;
          lines.push(
            `| \`${flag}\` | ${p.kind} | ${p.required ? "yes" : "no"} | ${p.location} | ${p.description ?? ""} |`,
          );
        }
        lines.push("");
      }
    }
  }

  return lines.join("\n");
}

function resolveDocsArgs(argv: string[]): { outFile: string; cliName: string } {
  const outIdx = argv.indexOf("--out");
  const outFile = outIdx !== -1 ? argv[outIdx + 1] : "COMMANDS.md";
  if (!outFile) {
    console.error("--out requires a path argument");
    process.exit(1);
  }
  const nameIdx = argv.indexOf("--name");
  const cliName = nameIdx !== -1 ? (argv[nameIdx + 1] ?? "cli") : "cli";
  return { outFile, cliName };
}

const argv = process.argv.slice(2);
const subcommand = argv[0];
const args = argv.slice(1);

if (subcommand === "docs") {
  const openapiIdx = args.indexOf("--openapi");
  const heyApiIdx = args.indexOf("--hey-api");
  const { outFile, cliName } = resolveDocsArgs(args);

  let entries: DocEntry[];

  if (openapiIdx !== -1) {
    const source = args[openapiIdx + 1];
    if (!source) {
      console.error("Usage: umbra docs --openapi <url-or-path> [--out <path>] [--name <cli-name>]");
      process.exit(1);
    }
    const spec = await loadSpec(source);
    entries = extractRoutes(spec);
  } else if (heyApiIdx !== -1) {
    const dir = args[heyApiIdx + 1];
    if (!dir) {
      console.error("Usage: umbra docs --hey-api <dir> [--out <path>] [--name <cli-name>]");
      process.exit(1);
    }
    entries = await docEntriesFromSdk(dir);
  } else {
    console.error(
      "Usage: umbra docs --openapi <url-or-path> | --hey-api <dir> [--out <path>] [--name <cli-name>]",
    );
    process.exit(1);
  }

  const docs = generateDocs(entries, cliName);
  mkdirSync(dirname(outFile), { recursive: true });
  writeFileSync(outFile, docs);
  console.log(`Generated ${outFile}`);
} else {
  const openapiIdx = argv.indexOf("--openapi");

  if (openapiIdx !== -1) {
    const source = argv[openapiIdx + 1];
    if (!source) {
      console.error("Usage: umbra --openapi <url-or-path> [--out <path>]");
      process.exit(1);
    }
    const outIdx = argv.indexOf("--out");
    const outFile = outIdx !== -1 ? argv[outIdx + 1] : "cli.gen.ts";
    if (!outFile) {
      console.error("--out requires a path argument");
      process.exit(1);
    }
    const pkgIdx = argv.indexOf("--package");
    const pkg = pkgIdx !== -1 ? (argv[pkgIdx + 1] ?? "@ihs7/umbra") : "@ihs7/umbra";
    const content = await generateSpecModule(source, pkg);
    mkdirSync(dirname(outFile), { recursive: true });
    writeFileSync(outFile, content);
    console.log(`Generated ${outFile}`);
  } else {
    const opts = parseArgs(argv);
    mkdirSync(dirname(opts.outFile), { recursive: true });
    writeFileSync(opts.outFile, generateCliRegistry(opts));
    console.log(`Generated ${opts.outFile}`);
  }
}
